export function feeCalc (amount, fx, price) {
  const VISAFeeRate = 1 + 0.011 // 1.1% - 1.0~1.1%. the more card base
  const SHINHANFeeRate = 0.0018 // 0.18% - credit card base

  if (fx === 'USD') {
    const outseaUsingAmount = roundDown(Number(amount) * VISAFeeRate, 2)

    const shfee = Math.floor(outseaUsingAmount * price * SHINHANFeeRate)
    return {
      value: roundDown(outseaUsingAmount * price, 0) + shfee,
      details: {
        fee: {
          VISA: outseaUsingAmount - amount,
          SHINHANCARD: shfee
        }
      }
    }
  } else {
    const rate = price // more clarified for math expression

    const exchangeResult = Number((rate * amount).toFixed(2))
    const visaFee = roundDown(exchangeResult * 0.011, 2)

    return {
      value: exchangeResult + visaFee,
      details: {
        fee: {
          VISA: visaFee
        }
      }
    }
  }
}

export function makeTable () {
  return [...Array(20).keys()]
    .map(item => [(item + 5) + '999', ''])
}

export function roundDown (number, digit) {
  return Math.floor(number * Math.pow(10, digit)) / Math.pow(10, digit)
}

export function makeDateCode (datetime) {
  const date = new Date(datetime)
  return date.getFullYear() + '-' +
    String(date.getMonth() + 1).padStart(2, 0) + '-' +
    String(date.getDate()).padStart(2, 0)
}

